 package ch01;

public class Literals {

	public static void main(String[] args) {
		/* 리터럴 : 프로그램의 일부로 사용하는 값 */
		int num1 = 20; // 20 : 리터럴(정수 리터럴)
		float f1 = 2.34f; //float형 리터럴 ('f' 또는 'F'를 붙여준다.)
		double d1 = 3.14; //double형 리터럴 (default가 double)
		long l1 = 323L; //int와 long을 구분하기 위해 'L' 또는 'l'을 붙여준다.)
		char c1 = 'A'; // char 리터럴
		String name = "홍길동"; // 문자열 리터럴
		// 정수에서는 default가 integer(int)임. : byte, short, char, long, int
		// 실수에서는 default가 double임. : float, double
		boolean flag = true;
		boolean flag2 = false;

	}

}
